package chapter03;

public class switch_pratice {

	public static void main(String[] args) {
		  int number =3 ;
		  
		  switch (number ){
			  case 3:
			  System.out.println("*");
			  case 2:
				System.out.println("*");  // number가 3이므로 
			  case 1:
				  System.out.println("*"); //break 문이 없으므
		  }
	}

}
